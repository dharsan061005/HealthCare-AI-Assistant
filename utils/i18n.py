def t(key: str, lang: str = "English") -> str:
    """
    Return the translated string for *key* in *lang*.
    Falls back to English if the key or language is missing.

    Args:
        key : A key from the _T dict (e.g. "save", "patient_name").
        lang: Full language label as shown in LANGUAGES list
              (e.g. "தமிழ் (Tamil)").

    Returns:
        Translated string, or the English fallback, or the key itself.
    """
    code    = _LANG_CODE.get(lang, "en")
    entry   = _T.get(key, {})
    return entry.get(code) or entry.get("en") or key


def get_lang(session_state) -> str:
    """
    Safely read the current UI language from Streamlit session_state.
    Returns "English" if not set.
    """
    return getattr(session_state, "ui_language", None) or "English"


